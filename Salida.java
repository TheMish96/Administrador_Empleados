    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

         if(ID_S.getText().length()==0){
           
           JOptionPane.showMessageDialog(null,"FABOR DE INGRESAR ID");
       } 
       else{
        String ids = ID_S.getText();
        int id = Integer.parseInt(ids);
        String sql = "select id_empleado from empleado where id_empleado =" + id + "";
        Statement st;
        try {
            st = n.createStatement();
            ResultSet rs = st.executeQuery(sql);

            if (rs.next()) {
                try {
                    Calendar c = Calendar.getInstance();
                    int hora = c.get(Calendar.HOUR);
                    int minuto = c.get(Calendar.MINUTE);

                    PreparedStatement pps = n.prepareStatement("INSERT INTO salida  VALUES (?,?,?,?)");
                    pps.setString(1, ID_S.getText());
                    pps.setInt(2, hora);
                    pps.setInt(3, minuto);
                    pps.setString(4, jComboBox1.getSelectedItem().toString());
                    pps.executeUpdate();
                    JOptionPane.showMessageDialog(null, "las horas han sido ingresadas ala base de datos");
                    limpiar();
                } catch (SQLException ex) {
                    System.out.println(" la excepcion es " + ex);
                    Logger.getLogger(Empleados.class.getName()).log(Level.SEVERE, null, ex);
                }

            }
            
            else{
            JOptionPane.showMessageDialog(null,"ID invalido");
           ID_S.setText("");
        }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "ID NO VALIDO");
            Logger.getLogger(MOSTRAR.class.getName()).log(Level.SEVERE, null, ex);
        }
        
         }
    }//GEN-LAST:event_jButton1ActionPerformed

